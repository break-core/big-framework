local ReplicatedStorage = game:GetService("ReplicatedStorage")

----------------------

local Directory = {}

----------------------


function starts_with(name, stringCheck)
    if #name < #stringCheck then
        return false
    end
    if stringCheck == "" then
        return true
    end
    if name:sub(1, #stringCheck) == stringCheck then
        return true
    end
    return false
end

--- Iterate through (physical) directory
local __DIRECTORY = ReplicatedStorage:WaitForChild("__DIRECTORY")
for _, dirFolder in ipairs(__DIRECTORY:GetChildren()) do

    --- Setup directory
    local dirName = dirFolder.Name
    Directory[dirName] = {}
    local dirData = Directory[dirName]

    --- Folder? Scan it!
    for _, module in ipairs(dirFolder:GetChildren()) do
        if module.ClassName ~= "ModuleScript" or module:GetAttribute("NOLOAD") then 
		     continue 
		end
        local moduleData = require(module)
        for key, value in pairs(moduleData) do
            dirData[key] = value
        end
    end
end

function Directory.NumberIDs(dirFolderName)
	return #ReplicatedStorage:WaitForChild("__DIRECTORY")[dirFolderName]:GetChildren()
end

----------------------

return Directory
